seunome = str(input('Digite o seu nome: '))
#Deixa a primeira letra sempre maiuscula
print('Nome próprio se escreve com letra maiúscula, ajeitei pra você: ' + seunome.capitalize())

#Imprime as tres primeiras letras
print('As três primeiras letras do seu nome são: ' + seunome.capitalize()[:3])

#Imprime o nome ao contrario
print('Seu nome ao contrário é: ' +seunome.capitalize()[::-1])

